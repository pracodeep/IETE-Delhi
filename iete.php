                        <p class="mt-n2 mb-30">Phenomenal developments in Telecommunications and Electronics in any countries were already influencing independent India engaged in its gigantic nation building efforts in 1947. The existing professional or scientific bodies in India at that time were not able to pay adequate attention to nurture and grow these specialized engineering areas at a rapid pace as required by the country. The need for a separate professional institution fully devoted to Telecommunications, and Electronics which could bring together various individuals or groups working in these areas became acute in the early '50s, as India Launched its programme of national development through five-year plans. It was visualized that such an institution could cope with the rapid developments in the above fields, coordinate and disseminate technical knowledge, facilitate exchange of views among the members, publish results of their aspirations to professional status and also serve as a focal point for the orderly growth of this specialized branch in the country.</p>
